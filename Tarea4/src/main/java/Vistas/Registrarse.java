    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String tel = txtTel.getText();
        String email = txtEmail.getText();
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String confirmPassword = String.valueOf(txtConfirmPassword.getPassword());
        System.out.println(password);
        System.out.println(confirmPassword);
        if (username.isEmpty() || password.isEmpty()|| nombre.isEmpty() || apellido.isEmpty() || tel.isEmpty() || email.isEmpty() || confirmPassword.isEmpty()) {
            String camposVacios = "Debe llenar los campos:";
            if(username.isEmpty()){
                camposVacios = camposVacios + " Username ";
            
            }
             if(password.isEmpty()){ 
                camposVacios = camposVacios + " Password ";
            
            }
              if(nombre.isEmpty()){
                camposVacios = camposVacios + " Nombre ";
            
            }
               if(apellido.isEmpty()){
                camposVacios = camposVacios + " Apellido ";
            
            }
                if(tel.isEmpty()){
                camposVacios = camposVacios + " telefono ";
            
            }
                 if(email.isEmpty()){
                camposVacios = camposVacios + " email ";
            
            }
                  if(confirmPassword.isEmpty()){
                camposVacios = camposVacios + " Confirmar Contrase単a ";
            
            }
            JOptionPane.showMessageDialog(this, camposVacios);
        } else {
            //start the login process
            if(password == null ? confirmPassword != null : !password.equals(confirmPassword)){
           JOptionPane.showMessageDialog(this, "Las contrase単a es diferente debe ser igual");
           
            }else{
                 SqlStatements sqlControl = new SqlStatements();
                try {
                    if(sqlControl.userSignIn(username)){
                        JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe");
                        
                    }else{
                        if(sqlControl.InsertUser(username, password, nombre , apellido, tel, email)){
                            JOptionPane.showMessageDialog(this, "Se inserto correctamente");
                        }else{
                            JOptionPane.showMessageDialog(this, "Ah ocurrido un error insertando los datos");
                        }
                    }  } catch (SQLException ex) {
                    Logger.getLogger(Registrarse.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            
             }

        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

