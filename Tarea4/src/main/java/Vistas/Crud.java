 private void initTable(){
        tableModel = new DefaultTableModel();
        jTable1.setModel(tableModel);
        tableModel.addColumn("id");
        tableModel.addColumn("Nombre");
        tableModel.addColumn("Apellido");
        tableModel.addColumn("Telefono");
        tableModel.addColumn("Email");
        tableModel.addColumn("Username");
        tableModel.addColumn("Password");
    }
    
    private void loadData(){
     
        tableModel.getDataVector().removeAllElements();
       
        tableModel.fireTableDataChanged();
        
        try{
              connection = con.getConnection();
         
            
          String query = "SELECT * FROM Users";
            
            Statement statement = (Statement) connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);
            
           
            while (resultSet.next()){
                Object[] escuela = new Object[7];
                 escuela[0] = resultSet.getString("id");
                escuela[1] = resultSet.getString("nombre");
                escuela[2] = resultSet.getString("apellido");
                escuela[3] = resultSet.getString("tel");
                escuela[4] = resultSet.getString("mail");
                escuela[5] = resultSet.getString("username");
                escuela[6] = resultSet.getString("password");
                tableModel.addRow(escuela);
            }
            resultSet.close();
            statement.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
