    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String tel = txtTel.getText();
        String email = txtEmail.getText();
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        try{
        if (username.isEmpty() || password.isEmpty()|| nombre.isEmpty() || apellido.isEmpty() || tel.isEmpty() || email.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "username / password should not be empty", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            SqlStatements sqlControl = new SqlStatements();
                try {
                    if(sqlControl.userSignIn(username)){
                        JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe");
                        
                    }else{
                        if(sqlControl.InsertUser(username, password, nombre , apellido, tel, email)){
                            JOptionPane.showMessageDialog(this, "Se inserto correctamente");
                        }else{
                            JOptionPane.showMessageDialog(this, "Ah ocurrido un error insertando los datos");
                        }
                    }  } catch (SQLException ex) {
                    Logger.getLogger(Registrarse.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            

        }
        }finally {
          loadData();
          reset();
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

